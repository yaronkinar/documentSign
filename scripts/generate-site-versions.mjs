#!/usr/bin/env node
/**
 * Build site version history from git commits.
 * Writes apps/web/lib/site-versions.generated.ts
 */

import { execSync } from 'child_process';
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const outFile = path.join(root, 'apps/web/lib/site-versions.generated.ts');

function readPackageVersion() {
  const pkg = JSON.parse(
    fs.readFileSync(path.join(root, 'package.json'), 'utf8'),
  );
  const match = /^(\d+\.\d+)/.exec(pkg.version ?? '0.1.0');
  return match?.[1] ?? '0.1';
}

function parseCommits(limit = 40) {
  const raw = execSync(
    `git log --format=%H%x1f%s%x1f%cI --max-count=${limit}`,
    { cwd: root, encoding: 'utf8' },
  ).trim();

  if (!raw) return [];

  const total = Number(
    execSync('git rev-list --count HEAD', { cwd: root, encoding: 'utf8' }).trim(),
  );
  const versionPrefix = readPackageVersion();

  return raw.split('\n').map((line, index) => {
    const [hash, message, date] = line.split('\x1f');
    const versionNumber = total - index;
    return {
      version: `${versionPrefix}.${versionNumber}`,
      hash: hash.slice(0, 7),
      fullHash: hash,
      date,
      message: message.trim(),
    };
  });
}

function buildFile(commits) {
  const current = commits[0] ?? null;
  const payload = { current, history: commits };
  const json = JSON.stringify(payload, null, 2);

  return `/* eslint-disable */
// Generated by scripts/generate-site-versions.mjs — do not edit manually.

export type SiteVersionEntry = {
  version: string;
  hash: string;
  fullHash: string;
  date: string;
  message: string;
};

export type SiteVersions = {
  current: SiteVersionEntry | null;
  history: SiteVersionEntry[];
};

export const siteVersions: SiteVersions = ${json} as const;
`;
}

function main() {
  const commits = parseCommits();
  const next = buildFile(commits);

  let previous = '';
  if (fs.existsSync(outFile)) {
    previous = fs.readFileSync(outFile, 'utf8');
  }

  if (next === previous) {
    process.stdout.write('site-versions: up to date\n');
    return;
  }

  fs.mkdirSync(path.dirname(outFile), { recursive: true });
  fs.writeFileSync(outFile, next, 'utf8');
  process.stdout.write(`site-versions: wrote ${commits.length} entries\n`);
}

main();
